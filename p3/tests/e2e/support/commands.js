Cypress.Commands.add("login", (email="jill@harvard.edu", password="asdfasdf") => {
  cy.visit('/account');
  cy.get('[data-test="email-input"]').clear().type(email);
  cy.get('[data-test="password-input"]').clear().type(password);
  cy.get('[data-test="login-button"]').click();
  cy.wait(2000);
});
